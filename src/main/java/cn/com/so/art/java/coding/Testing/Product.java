package cn.com.so.art.java.coding.Testing;

public class Product {

	private Color color;
	private int weight;

	public Product(Color color, int weight) {
		this.color = color;
		this.weight = weight;
	}

	public Color getColor() {
		return this.color;
	}

	public int getWeight() {
		return this.weight;
	}

}
